#include "mini_rt.h"

t_point	vec_cross(t_point v1, t_point v2)
{
	t_point	v;

	v.x = v1.y * v2.z - v1.z * v2.y;
	v.y = v1.z * v2.x - v1.x * v2.z;
	v.z = v1.x * v2.y - v1.y * v2.x;
	return (v);
}
